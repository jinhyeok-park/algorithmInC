#include "rush01.h"

void	ft_print_map(int **map)
{
	int		i;
	int		j;
	char	temp;

	i = 0;
	j = 0;
	while (i < 4)
	{
		j = 0;
		while (j < 4)
		{
			temp = map[i][j] + '0';
			write(1, &temp, 1);
			if (!((j + 1) % 4 == 0))
				write(1, " ", 1);
			j++;
		}
		write(1, "\n", 1);
		i++;
	}
}
