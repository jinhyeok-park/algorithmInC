char	*ft_strcat(char *dest, char *src)
{
	int		i;
	int		k;

	k = 0;
	i = 0;
	while (dest[i])
		i++;
	while (src[k])
	{
		dest[i] = src[k];
		i++;
		k++;
	}
	dest[i] = '\0';
	return (dest);
}
