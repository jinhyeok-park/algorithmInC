#include<unistd.h>

int	main(int ac, char *av[])
{
	int		i;
	int		k;

	i = 0;
	k = 0;
	if (ac > 1)
	{
		ac--;
		while (av[ac] && ac != 0)
		{
			k = 0;
			while (av[ac][k])
			{
				write(1, &av[ac][k], 1);
				k++;
			}
			i++;
			ac--;
			write(1, "\n", 1);
		}
	}
	return (0);
}
