#include <unistd.h>

void	ft_putstr(char *str)
{
	int		i;
	char	temp;

	temp = '0';
	i = 0;
	while (str[i] != '\0')
	{
		temp = str[i];
		write(1, &temp, 1);
		i++;
	}
}
